class AddressBook{
    //property
    firstName;
    lastName;
    address;
    city;
    state;
    zip;
    phone;
    email;

    constructor(firstName,lastName,address,city,state,zip,phone,email)
    {
        this.firstName=firstName;
        this.lastName=lastName;
        this.address=address;
        this.city=address;
        this.state=state;
        this.zip=zip;
        this.phone=phone;
        this.email=email;
    }

}